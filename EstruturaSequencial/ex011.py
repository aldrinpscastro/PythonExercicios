n1 = int(input('Digite o primeiro número inteiro: '))
n2 = int(input('Digite o segundo número interiro: '))
n3 = int(input('Digite um número real: '))
dpms = n1 * 2 * (n2 / 2)
stpr = n1 * 3 + n3
tec = n3 ** 3
print(f'O produto do dobro do primeiro com metade do segundo é: {dpms}.')
print(f'A soma do tripo do primeiro com o terceiro é: {stpr}.')
print(f'O terceiro elevado ao cubo é: {tec}.')